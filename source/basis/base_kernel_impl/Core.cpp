#include "Core.h"

namespace smeta3d
{
	/////////////////////////////////////////////////////////////////////
	///
	SP_ICore GetSingltonCore()
	{
		static SP_ICore ptrCore = nullptr;
		if (!ptrCore)
			ptrCore.reset(new CCore());
		return ptrCore;
	}

	/////////////////////////////////////////////////////////////////////
	///
	CCore::CCore()
	{
	}

	/////////////////////////////////////////////////////////////////////
	///
	CCore::~CCore()
	{
	}

	/////////////////////////////////////////////////////////////////////
	///
	bool CCore::Init()
	{
		if (IsInit())
			return false;

		m_bInit = true;
		return IsInit();
	}
	/////////////////////////////////////////////////////////////////////
	///
	bool CCore::IsInit() const
	{
		return m_bInit;
	}
	/////////////////////////////////////////////////////////////////////
	///
	void CCore::DeInit()
	{
		if (!IsInit())
			return;

		m_bInit = false;
	}
}
